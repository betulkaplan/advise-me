import Head from "next/head";
import { useEffect, useState } from "react";
import packageJson from "../package.json";
type User = {
  id: string;
  email: string;
  name: string;
};

export default function Home() {
  const [users, setUsers] = useState<User[]>([]);
  useEffect(() => {
    fetch("api/hello")
      .then((res) => res.json())
      .then((res) => {
        console.log("res:", res);
        setUsers(res);
      });
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App </title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <div>
          <form
            onSubmit={(e: React.FormEvent<HTMLFormElement>) => {
              e.preventDefault();
              const email: any = e.currentTarget.elements[0];
              const name: any = e.currentTarget.elements[1];
              fetch("api/hello", {
                method: "POST",
                body: JSON.stringify({
                  email: email.value,
                  name: name.value,
                }),
              })
                .then((res) => res.json())
                .then((res) => {
                  console.log("res:", res);
                });
            }}
          >
            <label htmlFor="email">Email</label>
            <input type="email" id="email" name="fav_language" />
            <br />
            <label htmlFor="name">Name</label>
            <input type="text" id="name" name="fav_language" />
            <br />
            <button className="bg-red-200 m-2 p-2 rounded" type="submit">
              Submit
            </button>
          </form>
        </div>
        <div className="bg-red-300">
          <h3>Users</h3>
          {users.map((u, index) => {
            return (
              <div key={index}>
                {u.email} - {u.name}
              </div>
            );
          })}
        </div>
      </main>
    </>
  );
}
